<h3>Buying with Gift Certificates</h3>

<p>If you have funds in your Gift Certificate Account, you can use those funds to purchase other items in our store.</p>
<p>At the checkout stage, an extra box will appear. Enter the amount to apply from the funds in your Gift Certificate Account.
Please note, you will still have to select another payment method if there is not enough in your Gift Certificate Account to
cover the cost of your purchase. If you have more funds in your Gift Certificate Account than the total cost of your purchase
the balance will be left in your Gift Certificate Account for the future.</p>
