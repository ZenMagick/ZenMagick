<h3>How to Send Gift Certificates</h3>

<p>To send a Gift Certificate you need to go to our Send Gift Certificate Page. You can find the link to this page in the 
Shopping Cart Box in the right hand column of each page.</p>

<p>When you send a Gift Certificate, you need to specify the following:</p>
<ul>
  <li>The name of the person you are sending the Gift Certificate to.</li>
  <li>The email address of the person you are sending the Gift Certificate to.</li>
  <li>The amount you want to send. (Note you don't have to send the full amount that is in your Gift Certificate Account.)</li>
  <li>A short message which will appear in the email.</li>
</ul>
<p>Please ensure that you have entered all of the information correctly, although you will be given the opportunity to 
change this as much as you want before the email is actually sent.</p>
